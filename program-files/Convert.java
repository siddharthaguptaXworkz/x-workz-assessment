public class Convert{
	public static void main(String[] args) {
		boolean isMale = Boolean.parseBoolean("False");
		System.out.println("isMale "+isMale);
		byte numHoursInDay = Byte.parseByte("24");
		System.out.println("numHoursInDay "+numHoursInDay);
		short daysInAYear = Short.parseShort("365");
		System.out.println("daysInAYear "+daysInAYear);
		int secondsInDay = Integer.parseInt("86400");
		System.out.println("secondsInDay "+secondsInDay);
		long populationIndia = Long.parseLong("1352600000");
		System.out.println("populationIndia "+populationIndia);
		float percentageOfStudentsWithBPlus = Float.parseFloat("0.32F");
		System.out.println("percentageOfStudentsWithBPlus "+percentageOfStudentsWithBPlus);
		double  massOfHydrogen = Double.parseDouble("1.67e-27d");
		System.out.println("massOfHydrogen "+massOfHydrogen);
	
	}
}